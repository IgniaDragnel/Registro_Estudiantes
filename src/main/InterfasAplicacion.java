       private void cargarEscuelas(int busca) {
        int index = 1;
        list_escuelas.removeAllItems();
        list_escuelas.addItem("Selecciona una Escuela");
        for(EscuelasDS escu: EscuelasBDD.getListEscuelas()){
            list_escuelas.addItem(escu);
            if(escu.getId_escuela()== busca){
                list_escuelas.setSelectedIndex(index);
            }
            index++;
        }
    }
