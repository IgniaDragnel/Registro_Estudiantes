    private void buscarGeneralActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarGeneralActionPerformed
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("ID");
        modelo.addColumn("Cedula");
        modelo.addColumn("Nombre");
        modelo.addColumn("Fecha Nacimiento");
        modelo.addColumn("Direccion");
        modelo.addColumn("Email");
        modelo.addColumn("Celular");
        modelo.addColumn("Observaciones");
        table_general.setModel(modelo);
        Object[] arrAlumnos = new Object[8];
        for(AlumnosDS alum: AlumnosBDD.getListAlumnos()){
            arrAlumnos[0] = alum.getId_alu();
            arrAlumnos[1] = alum.getCedula_alu();
            arrAlumnos[2] = alum.getNombre_alu();
            arrAlumnos[3] = alum.getFechaNac_alu();
            arrAlumnos[4] = alum.getDireccion_alu();
            arrAlumnos[5] = alum.getEmail_alu();
            arrAlumnos[6] = alum.getCelular_alu();
            arrAlumnos[7] = alum.getObservaciones_alu();
            modelo.addRow(arrAlumnos);
        }
        table_general.setModel(modelo);
    }//GEN-LAST:event_buscarGeneralActionPerformed

